package com.torontomans.commute;

public class Coordinate {
    float lat;
    float lng;

    public void setLat(float lat) {
        this.lat = lat;
    }

    public void setLng(float lng) {
        this.lng = lng;
    }

    public float setLat() {
        return this.lat;
    }

    public float setLng() {
        return this.lng;
    }
}

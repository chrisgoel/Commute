package com.torontomans.commute;

public class TextVal {
    String text;
    int value;

    public void setText(String text) {
        this.text = text;
    }

    public void setValue(int value) {
        this.value = value;
    }

    public String getText() {
        return this.text;
    }

    public int getValue() {
        return this.value;
    }
}
